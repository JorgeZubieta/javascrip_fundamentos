// conjunción lógica (también llamada y lógico, o and por su nombre en inglés), que sólo retorna verdadero cuando todas las expresiones que opera son verdaderas.

function esPeripatetico(profesion, nacionalidad, cantidadDeKilometrosCaminadosPorDia){
    return profesion==="filosofo" && nacionalidad==="griego" && cantidadDeKilometrosCaminadosPorDia > 2;
}
// Ahora te toca a vos! Definí la función signo, que dado un número nos retorne:
// 1 si el número es positivo, 0 si el número es cero, -1 si el número es negativo_    

function signo(a,b){
    if (a > b || a > 0){
        return 1;
    } else
    
    if (a === 0){
        return 0;
    } else 
    
    if (a<b || a<0) {
        return -1;
    } 
}
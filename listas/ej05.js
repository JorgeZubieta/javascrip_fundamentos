// Empecemos por lo fácil: saber cuántos elementos hay en la lista.
// Esto lo podemos hacer utilizando la función longitud,
// de forma similar a lo que hacíamos con los strings.

// cuántos elementos hay en la lista con longitud!

var numerosDeLoteria = [2,  11, 17, 32, 36, 39];

// xConsola

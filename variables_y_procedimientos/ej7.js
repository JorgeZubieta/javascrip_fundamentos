// Definí las funciones perimetroCirculo y areaCirculo que reciben el radio de un círculo y a partir del mismo nos retornan su perímetro y su área.
//  ¡Dame una pista!
// El perímetro se calcula como dos veces pi por el radio de un círculo. El área, en cambio, es el resultado de hacer pi por radio por radio.

function perimetroCirculo(radio){
    return 3.14159265358979*2*radio;
}

function areaCirculo(radio){
    return 3.14159265358979*radio*radio;
}